package Ausbildungsbeispiele.Basic;

public class Multiplications {
    public static void main(String[] args) {



        for (int a = 1; a <= 10; a++){
            System.out.println("Times " + a + ":\n");
            for(int b = 1; b <= 10;b++) {

                System.out.println(b + "*" + a + "=" + (a*b));

            }
        }




    }
}
